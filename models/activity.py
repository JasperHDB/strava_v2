class Activity:
    def __init__(self, athlete_id, athlete_name, name, distance, moving_time, total_elevation_gain, type, sport_type,
                 start_date_local, achievement_count, kudos_count, comment_count, athlete_count, photo_count):
        self.athlete_id = athlete_id
        self.athlete_name = athlete_name
        self.name = name
        self.distance = distance
        self.moving_time = moving_time
        self.total_elevation_gain = total_elevation_gain
        self.type = type
        self.sport_type = sport_type
        self.start_date_local = start_date_local
        self.achievement_count = achievement_count
        self.kudos_count = kudos_count
        self.comment_count = comment_count
        self.athlete_count = athlete_count
        self.photo_count = photo_count
